def obtener_compradores(compras_diccionario):
    compras_diccionario = {
        '2000': compras_diccionario['2000', 'Name'],
        '2001': compras_diccionario['2001', 'Name'],
        '2002': compras_diccionario['2002', 'Name'],
        '2003': compras_diccionario['2003', 'Name'],
        '2004': compras_diccionario['2004', 'Name'],
        '2005': compras_diccionario['2005', 'Name']
    }

    return compras_diccionario.key(), compras_diccionario.values()

def obtener_compradores_anio(info, name):
    resultado = list(filter(lambda item: item['Name']== name, info))
    return resultado



